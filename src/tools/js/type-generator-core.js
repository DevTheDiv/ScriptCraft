'use strict';

/**
 * TypeScript Type Generator for ScriptCraft.
 * Uses Java Reflection to generate .d.ts files from Java classes.
 */

var Modifier = java.lang.reflect.Modifier;
var File = java.io.File;
var FileUtils = org.apache.commons.io.FileUtils;

function TypeGenerator() {
    this.classes = {};
    this.queue = [];
    this.processed = {};
}

TypeGenerator.prototype.mapType = function(javaType) {
    if (javaType.isPrimitive()) {
        var name = javaType.name;
        if (name === 'boolean') return 'boolean';
        if (name === 'void') return 'void';
        return 'number'; // int, long, float, etc.
    }

    if (javaType.isArray()) {
        return this.mapType(javaType.componentType) + '[]';
    }

    var fullName = javaType.name;
    if (fullName === 'java.lang.String') return 'string';
    if (fullName === 'java.lang.Object') return 'any';
    if (fullName === 'java.lang.Boolean') return 'boolean';
    if (fullName.startsWith('java.lang.Number') || 
        fullName === 'java.lang.Integer' || 
        fullName === 'java.lang.Long' || 
        fullName === 'java.lang.Double') return 'number';

    // Extract short name for the interface
    var parts = fullName.split('.');
    var shortName = parts[parts.length - 1].replace(/\$/g, '_');
    
    // Add to queue if not processed
    if (!this.processed[fullName] && fullName.startsWith('org.bukkit')) {
        this.queue.push(fullName);
    }
    
    return shortName;
};

TypeGenerator.prototype.generateClass = function(className) {
    if (this.processed[className]) return;
    this.processed[className] = true;

    try {
        var clz = java.lang.Class.forName(className);
        var parts = className.split('.');
        var shortName = parts[parts.length - 1].replace(/\$/g, '_');
        
        var output = 'interface ' + shortName;
        
        // Handle Inheritance
        var superClz = clz.getSuperclass();
        if (superClz && superClz.name !== 'java.lang.Object' && superClz.name.startsWith('org.bukkit')) {
            output += ' extends ' + this.mapType(superClz);
        }
        
        output += ' {\n';

        // Methods
        var methods = clz.getDeclaredMethods();
        for (var i = 0; i < methods.length; i++) {
            var method = methods[i];
            if (!Modifier.isPublic(method.getModifiers())) continue;

            var methodName = method.getName();
            var params = method.getParameters();
            var paramStrs = [];
            for (var j = 0; j < params.length; j++) {
                paramStrs.push('arg' + j + ': ' + this.mapType(params[j].getType()));
            }
            
            output += '    ' + methodName + '(' + paramStrs.join(', ') + '): ' + this.mapType(method.getReturnType()) + ';\n';
        }

        output += '}\n\n';
        this.classes[className] = output;
    } catch (e) {
        // console.error('Failed to process class: ' + className + ' - ' + e);
    }
};

TypeGenerator.prototype.run = function(startClasses) {
    this.queue = startClasses;
    while (this.queue.length > 0) {
        var next = this.queue.shift();
        this.generateClass(next);
    }

    var finalOutput = '/** Autogenerated ScriptCraft Types **/\n\n';
    for (var clz in this.classes) {
        finalOutput += this.classes[clz];
    }
    return finalOutput;
};

module.exports = TypeGenerator;
